#include <stdio.h>

int main() {
    //1、按实际运算步骤，打印出（（13<<2）| 4） ^ 15 每步的结果。
    printf("第一步为结果%d\n",13 << 2);
    printf("第二步的结果为%d\n",(13 << 2) | 4);
     printf("第三步的结果为%d\n",((13 << 2) | 4) ^ 15);
    //2、按实际运算步骤，打印出~(14 | 7) & 9 每步结果。
    printf("=================================================\n");
    printf("第一步为结果%d\n",14 | 7);
    printf("第二步为结果%d\n",~(14 | 7));
    printf("第三步为结果%d\n",~(14 | 7) & 9);
    return 0;
}