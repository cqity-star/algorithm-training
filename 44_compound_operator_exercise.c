/*
    练习2：
    1、定义两个int类型变量c和d，分别赋初始值为3和5。
    2、利用复合运算符和自增自减运算符，使得c和自增的d相乘后，c变为15，d变为6，打印出结果。
    3、利用复合运算符和自增自减运算符，使得d和自减的c相加后，d变为20，c变为14，打印出结果。

    打印格式：c = ..., d = ...
*/
#include <stdio.h>

int main() {
    int c = 3;
    int d = 5;

    c *= d++;
    printf("c = %d,d = %d\n",c,d);
    d += --c;
    printf("c = %d,d = %d\n",c,d);
}
