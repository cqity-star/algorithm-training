//十进制占位符%d
//八进制占位符%o
//十六进制占位符%x或%X
#include <stdio.h>

int main() {
    printf("500的八进制为%o\n",500);
    printf("500的十六进制为%x\n",500);
    printf("500的十六进制为%X\n",500);
    //带前缀表示 加#
    printf("500的八进制为%#o\n",500);
    printf("500的十六进制为%#x\n",500);
    printf("500的十六进制为%#X\n",500);
    //转为十进制 加前缀
    printf("八进制数764的十进制数为；%d\n",0764);
    printf("十六进制数f4的十进制数为；%d\n",0x1f4);
    return 0;
}